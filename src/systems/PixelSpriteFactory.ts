// 绘制函数
// ============================
function drawPixelArt(g: PIXI.Graphics, matrix: number[][], palette: number[], pixelSize: number, offsetX: number, offsetY: number): void {
    for (let r = 0; r < matrix.length; r++) {
        for (let c = 0; c < matrix[r].length; c++) {
            const colorIdx = matrix[r][c];
            if (colorIdx === 0) continue;
            const color = palette[colorIdx - 1] || 0xffffff;
            g.beginFill(color);
            g.drawRect(offsetX + c * pixelSize, offsetY + r * pixelSize, pixelSize, pixelSize);
            g.endFill();
        }
    }
}

